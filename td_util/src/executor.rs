//! Adapter to bridge the gap between sync and async code.

use std::future::Future;

pub fn run_as_sync<F: Future>(future: F) -> F::Output {
    tokio::runtime::Builder::new_current_thread()
        .enable_all()
        .build()
        .unwrap()
        .block_on(future)
}
